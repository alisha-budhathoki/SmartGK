package com.example.smartgk.model;

public class NewCourses {

    int imageN;
    String courseNameN, coursePriceN;

    public NewCourses() {
        this.imageN = imageN;
        this.courseNameN = courseNameN;
        this.coursePriceN = coursePriceN;
    }

    public int getImageN() {
        return imageN;
    }

    public void setImageN(int imageN) {
        this.imageN = imageN;
    }

    public String getCourseNameN() {
        return courseNameN;
    }

    public void setCourseNameN(String courseNameN) {
        this.courseNameN = courseNameN;
    }

    public String getCoursePriceN() {
        return coursePriceN;
    }

    public void setCoursePriceN(String coursePriceN) {
        this.coursePriceN = coursePriceN;
    }
}
