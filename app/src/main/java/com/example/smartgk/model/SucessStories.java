package com.example.smartgk.model;

public class SucessStories {
    int sucessImg;
    String sucessName, sucessDesc;

    public SucessStories() {
        this.sucessImg = sucessImg;
        this.sucessName = sucessName;
        this.sucessDesc = sucessDesc;
    }

    public int getSucessImg() {
        return sucessImg;
    }

    public void setSucessImg(int sucessImg) {
        this.sucessImg = sucessImg;
    }

    public String getSucessName() {
        return sucessName;
    }

    public void setSucessName(String sucessName) {
        this.sucessName = sucessName;
    }

    public String getSucessDesc() {
        return sucessDesc;
    }

    public void setSucessDesc(String sucessDesc) {
        this.sucessDesc = sucessDesc;
    }
}
